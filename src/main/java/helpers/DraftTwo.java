package helpers;

public class DraftTwo {

    public static void main(String[] args) {

        //Do while
        int i = 1;
        do {
            System.out.printf("%d ", i);
            i++;
        } while (i <= 10);

        System.out.printf("%n%n");

        //While
        int j = 1;
        while(j <= 10) {
            System.out.printf("%d ", j);
            j++;
        }
    }
}
